package com.oops;

public class TestSwitchCase {
	
public static void main(String[] args) {
	char ch='2';
	int i=2;
	switch (i) {
	case 1:
		System.out.println("1");
		break;
	case 2:
		System.out.println("2");
		break;
	case 3:
		System.out.println("ch");

	default:
		System.out.println("default");
		break;
	}
}
}
